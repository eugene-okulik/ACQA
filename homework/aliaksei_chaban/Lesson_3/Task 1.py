# Даны 2 числа a и b. Получить их сумму, разность и произведение

def sum_of_variables(a, b):
    return a + b

def difference_of_variables(a, b):
    return a - b

def product_of_variables(a, b):
    return a * b

first_value = 7
second_value = 4

print(sum_of_variables(first_value, second_value))
print(difference_of_variables(first_value, second_value))
print((product_of_variables(first_value, second_value)))
